package OOP_Basic;

public class Animal {

    public String name;
    public String color;
    public String breed;
    public int age;


    public void Name (String name) {
        System.out.println("The name of the Dog is " + name);
    }

    public void Color (String color) {
        System.out.println("The color of the Dog is " + color);
    }

    public void Breed (String breed) {
        System.out.println("The breed of the Dog is " + breed);
    }

    public void Age (int age) {
        System.out.println("The age of the Dog is " + age);
    }
}
